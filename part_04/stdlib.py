def sum_all(*args):
    return sum(args)


def add(x, y):
    return x + y


def sub(x, y):
    return x - y


def mul(x, y):
    return x * y


def div(x, y):
    return x / y


def get_pow(base, exp):
    return base ** exp


def get_max(*args):
    return max(args)


def get_min(*args):
    return min(args)
